public class JavaStringDemo {
 public static void main(String[] args) {
 String x = "airplane";
System.out.println( x.charAt(2) ); // output is 'r'
String y = "book";
System.out.println( y.concat(" author")); 
// output is "book author"
String z = "01234567";
System.out.println( z.length() ); // returns "8"
 }
}


